export const strings: Record<Lang, StringSet> = { el: baseEl, en, es, fr, de, it };

export function t(lang: Lang, key: StringKey): string {
  return strings[lang]?.[key] || baseEl[key];
}

export const VALID_LANGS: Lang[] = ['el', 'en', 'es', 'fr', 'de', 'it'];
export function isLang(v: any): v is Lang {
  return typeof v === 'string' && (VALID_LANGS as string[]).includes(v);
}

/**
 * Localize a multi-language field from a routine/step/option.
 * Looks up: item[`${field}_${lang}`] -> item.{names|titles|labels}[lang]
 *   -> falls back to _en -> _el.
 */
export function loc(
  item: any,
  field: 'name' | 'title' | 'label',
  lang: Lang
): string {
  if (!item) return '';
  // Direct localized fields (name_el, name_en)
  const direct = item[`${field}_${lang}`];
  if (direct) return direct;
  // Nested dict (names/titles/labels)
  const dictKey =
    field === 'name' ? 'names' : field === 'title' ? 'titles' : 'labels';
  const nested = item[dictKey]?.[lang];
  if (nested) return nested;
  // Fallbacks
  return item[`${field}_en`] || item[`${field}_el`] || '';
}

/** Count label in current language (e.g., "6 βήματα" / "6 steps"). */
export function stepsLabel(lang: Lang, count: number): string {
  const labels: Record<Lang, string> = {
    el: count === 1 ? 'βήμα' : 'βήματα',
    en: count === 1 ? 'step' : 'steps',
    es: count === 1 ? 'paso' : 'pasos',
    fr: count === 1 ? 'étape' : 'étapes',
    de: count === 1 ? 'Schritt' : 'Schritte',
    it: count === 1 ? 'passo' : 'passi',
  };
  return `${count} ${labels[lang]}`;
}
